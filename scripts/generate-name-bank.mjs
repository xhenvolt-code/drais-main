// ─── Auto-phonetic converter (Luganda rules) ─────────────────────────────────
function lugandaPhonetic(name) {
  return name
    .toLowerCase()
    // Double consonants → emphasize
    .replace(/kk/g, 'kk').replace(/gg/g, 'gg').replace(/mm/g, 'mm')
    .replace(/nn/g, 'nn').replace(/ss/g, 'ss').replace(/tt/g, 'tt')
    .replace(/bb/g, 'bb').replace(/dd/g, 'dd').replace(/ff/g, 'ff')
    .replace(/ll/g, 'll').replace(/pp/g, 'pp').replace(/rr/g, 'rr')
    .replace(/zz/g, 'zz')
    // Special digraphs
    .replace(/ny/g, 'ny').replace(/ng'/g, 'ng')
    .replace(/nj/g, 'nj').replace(/nk/g, 'nk').replace(/nt/g, 'nt')
    .replace(/mp/g, 'mp').replace(/mb/g, 'mb').replace(/ng/g, 'ng')
    .replace(/mw/g, 'mw').replace(/bw/g, 'bw').replace(/kw/g, 'kw')
    .replace(/gw/g, 'gw').replace(/tw/g, 'tw').replace(/dw/g, 'dw')
    .replace(/sw/g, 'sw').replace(/zw/g, 'zw').replace(/lw/g, 'lw')
    .replace(/rw/g, 'rw').replace(/jw/g, 'jw').replace(/hw/g, 'hw')
    // Vowels (order: u before others that could match)
    .replace(/u/g, 'oo').replace(/i/g, 'ee').replace(/e/g, 'eh')
    .replace(/o/g, 'oh').replace(/a/g, 'ah')
    // Clean up double-converted vowels
    .replace(/ooh/g, 'oh').replace(/ohh/g, 'oh')
    .replace(/aah/g, 'ah').replace(/ehh/g, 'eh').replace(/eeh/g, 'ee')
    // Add hyphens between syllables (simple: split on consonant clusters)
    .replace(/([aeiou])([^aeiou\-\s]{2,})([aeiou])/g, '$1-$2$3')
    .trim();
}

// Friendlier phonetic for common patterns
function ph(s) { return lugandaPhonetic(s); }

function entry(name, phonetic, origin, gender) {
  const cleanName = name.replace(/\d+$/, '');
  return { name: cleanName, phonetic_hint: phonetic || ph(cleanName), origin, gender_bias: gender };
}

function prefixed(prefix, roots, phPrefix, origin, gender) {
  return roots.map(root => ({
    name: prefix + root,
    phonetic_hint: phPrefix + '-' + ph(root),
    origin,
    gender_bias: gender,
  }));
}

