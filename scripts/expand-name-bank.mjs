import { readFileSync, writeFileSync } from 'fs';

const PATH = './src/data/nameBank.json';
const existing = JSON.parse(readFileSync(PATH, 'utf8'));
const seen = new Set(existing.map(e => e.name.toLowerCase()));

function add(name, phonetic, origin, gender) {
  const k = name.toLowerCase();
  if (seen.has(k)) return null;
  seen.add(k);
  return { name, phonetic_hint: phonetic, origin, gender_bias: gender };
}

function bulk(arr) { return arr.map(([n,p,o,g]) => add(n,p,o,g)).filter(Boolean); }

